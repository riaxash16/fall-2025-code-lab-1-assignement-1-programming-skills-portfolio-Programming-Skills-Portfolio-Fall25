for i in range(51): #loop ranging from 0 to 50
    print(i) #printing the number
print("\n") #new line

for i in range(50, -1, -1):  #loop ranging from 50 to 0
    print(i) #printing the number
print("\n") #new line

for i in range(30, 51): #loop ranging from 30 to 50
    print(i) #printing the number
print("\n") #new line

for i in range(50, 9, -2): #loop ranging from 50 to 10 with step of -2
    print(i) #printing the number
print("\n") #new line

for i in range(100, 201, 5): #loop ranginging from 100 to 200 with step of 5
    print(i) #printing the number
print("\n") #new line